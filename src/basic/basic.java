package basic;

public class basic {
    public static void main(String[] args){
        //\t一个制表位，实现对齐功能
        System.out.println("北京\t天津\t上海");
        //\n换行符
        System.out.println("jack\thaha\t");
        //转义\
        System.out.println("c:\\dcd\\a.txt");
        System.out.println("卷起来，\"疯狂卷\"");
        //\r回车
        System.out.println("哈哈哈哈哈\r你号");
    }
}
